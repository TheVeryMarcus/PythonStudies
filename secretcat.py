# Function to expand the pixel structure
def expand_pixels(pixel_structure):
    expanded_lines = []
    for line in pixel_structure:
        expanded_line = ''.join([symbol * count for symbol, count in line])
        expanded_lines.append(expanded_line)
    return "\n".join(expanded_lines)

